
import pylibxc
import pytest
import numpy
from pylibxc.example_densities import test_data


def test_mgga_x_2d_prhg07_prp10_H_2_vrho():
    # Prepare the input
    inp = test_data["H"]

    # Get the functional
    feval = pylibxc.LibXCFunctional("mgga_x_2d_prhg07_prp10", 2)

    # Evaluate the data
    out = feval.compute(inp, do_exc=False, do_vxc=True, do_fxc=False, do_kxc=False, do_lxc=False)
    tgt = out["vrho"].flatten()
    ref_tgt = numpy.asarray([-1.824222553926678e+00, -6.766874158199460e-10, -1.548850546606748e+00, -6.766874158199460e-10, -7.759419022243069e-01, -6.766874158199460e-10, -2.732666697333314e-01, -6.766874158199460e-10, -1.099807821638067e-01, -6.766874158199460e-10])
    error = numpy.max(numpy.abs(tgt-ref_tgt))/(1.0+numpy.max([numpy.abs(tgt), numpy.abs(ref_tgt)]))
    assert error < 5e-05


