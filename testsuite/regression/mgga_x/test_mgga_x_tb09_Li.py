
import pylibxc
import pytest
import numpy
from pylibxc.example_densities import test_data


def test_mgga_x_tb09_Li_2_vrho():
    # Prepare the input
    inp = test_data["Li"]

    # Get the functional
    feval = pylibxc.LibXCFunctional("mgga_x_tb09", 2)

    # Evaluate the data
    out = feval.compute(inp, do_exc=False, do_vxc=True, do_fxc=False, do_kxc=False, do_lxc=False)
    tgt = out["vrho"].flatten()
    ref_tgt = numpy.asarray([-2.167476784795455e+00, -2.170183818159405e+00, -1.824581085705391e+00, -1.827187089810056e+00, -4.397007742044033e-01, -4.457310584490382e-01, -2.517562313668584e-01, 1.728818113227116e-01, -9.547857924656376e-02, 2.233496377863980e-01, 2.828261330416276e+00, 1.774947411103677e-01, 1.131667052213888e+02, 2.358971570855556e-01])
    error = numpy.max(numpy.abs(tgt-ref_tgt))/(1.0+numpy.max([numpy.abs(tgt), numpy.abs(ref_tgt)]))
    assert error < 5e-05


