
import pylibxc
import pytest
import numpy
from pylibxc.example_densities import test_data


def test_mgga_x_rpp09_Li_2_vrho():
    # Prepare the input
    inp = test_data["Li"]

    # Get the functional
    feval = pylibxc.LibXCFunctional("mgga_x_rpp09", 2)

    # Evaluate the data
    out = feval.compute(inp, do_exc=False, do_vxc=True, do_fxc=False, do_kxc=False, do_lxc=False)
    tgt = out["vrho"].flatten()
    ref_tgt = [-2.750957816105114e+00, -2.766900947854239e+00, -2.391863971683761e+00, -2.415623793952840e+00, -8.797506036443236e-01, -9.671914940416854e-01, -2.847040627580722e-01, -3.351745062878287e-01, -1.732291246911965e-01, -9.626149255702711e-02, 2.791896978369863e+00, -3.372392213010588e-01, 1.131662183048723e+02, -8.034496117975480e-02]
    error = numpy.max(numpy.abs(tgt-ref_tgt))/(1.0+numpy.max([numpy.abs(tgt), numpy.abs(ref_tgt)]))
    assert error < 5e-05


