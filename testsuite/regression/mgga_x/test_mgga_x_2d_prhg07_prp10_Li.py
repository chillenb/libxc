
import pylibxc
import pytest
import numpy
from pylibxc.example_densities import test_data


def test_mgga_x_2d_prhg07_prp10_Li_2_vrho():
    # Prepare the input
    inp = test_data["Li"]

    # Get the functional
    feval = pylibxc.LibXCFunctional("mgga_x_2d_prhg07_prp10", 2)

    # Evaluate the data
    out = feval.compute(inp, do_exc=False, do_vxc=True, do_fxc=False, do_kxc=False, do_lxc=False)
    tgt = out["vrho"].flatten()
    ref_tgt = numpy.asarray([-8.508880041708913e+00, -8.520382727267387e+00, -4.857895003256468e+00, -4.864094929807901e+00, -1.032100037463298e+00, -1.029436840640052e+00, -2.314346910836484e-01, -2.608699863634249e-01, -1.196014224094069e-01, -7.735367752836700e-02, -9.034556612103851e-01, -2.641644450518751e-01, -1.336926300559676e+01, -6.676771705381972e-02])
    error = numpy.max(numpy.abs(tgt-ref_tgt))/(1.0+numpy.max([numpy.abs(tgt), numpy.abs(ref_tgt)]))
    assert error < 5e-05


