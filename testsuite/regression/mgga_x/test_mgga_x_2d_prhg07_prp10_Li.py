
import pylibxc
import pytest
import numpy
from pylibxc.example_densities import test_data


def test_mgga_x_2d_prhg07_prp10_Li_2_vrho():
    # Prepare the input
    inp = test_data["Li"]

    # Get the functional
    feval = pylibxc.LibXCFunctional("mgga_x_2d_prhg07_prp10", 2)

    # Evaluate the data
    out = feval.compute(inp, do_exc=False, do_vxc=True, do_fxc=False, do_kxc=False, do_lxc=False)
    tgt = out["vrho"].flatten()
    ref_tgt = numpy.asarray([-8.586166225911727e+00, -8.596228468954175e+00, -5.043145720132048e+00, -5.049360712966080e+00, -8.686318328550483e-01, -8.687818293092932e-01, -2.429858963862673e-01, -1.707221428938500e-01, -1.026217611859390e-01, -2.384319324204063e-05, -1.724657380924709e-01, -4.858845456613169e+80, -5.851578919805252e-02, -3.660075629577190e+126])
    error = numpy.max(numpy.abs(tgt-ref_tgt))/(1.0+numpy.max([numpy.abs(tgt), numpy.abs(ref_tgt)]))
    assert error < 5e-05


