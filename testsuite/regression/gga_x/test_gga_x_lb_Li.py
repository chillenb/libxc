
import pylibxc
import pytest
import numpy
from pylibxc.example_densities import test_data


def test_gga_x_lb_Li_2_vrho():
    # Prepare the input
    inp = test_data["Li"]

    # Get the functional
    feval = pylibxc.LibXCFunctional("gga_x_lb", 2)

    # Evaluate the data
    out = feval.compute(inp, do_exc=False, do_vxc=True, do_fxc=False, do_kxc=False, do_lxc=False)
    tgt = out["vrho"].flatten()
    ref_tgt = numpy.asarray([-2.798525926428237e+00, -2.800473896529365e+00, -2.122369608195878e+00, -2.123829824759418e+00, -8.197774375921174e-01, -8.201345509480685e-01, -2.395862491129465e-01, -2.590476411098520e-01, -1.509725539725629e-01, -1.070735613404400e-01, -2.607772930502912e-01, -2.640035134501724e-01, -1.102698323644574e-01, -9.833361362093443e-02])
    error = numpy.max(numpy.abs(tgt-ref_tgt))/(1.0+numpy.max([numpy.abs(tgt), numpy.abs(ref_tgt)]))
    assert error < 5e-05


