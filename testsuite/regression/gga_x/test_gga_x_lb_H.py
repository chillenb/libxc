
import pylibxc
import pytest
import numpy
from pylibxc.example_densities import test_data


def test_gga_x_lb_H_2_vrho():
    # Prepare the input
    inp = test_data["H"]

    # Get the functional
    feval = pylibxc.LibXCFunctional("gga_x_lb", 2)

    # Evaluate the data
    out = feval.compute(inp, do_exc=False, do_vxc=True, do_fxc=False, do_kxc=False, do_lxc=False)
    tgt = out["vrho"].flatten()
    ref_tgt = [-8.348081538280193e-01, -1.284862555151638e-05, -9.066962911515983e-01, -1.284862555151638e-05, -6.191503236975300e-01, -1.284862555151638e-05, -2.811156211931344e-01, -1.284862555151638e-05, -1.174525938525839e-01, -1.284862555151638e-05]
    error = numpy.max(numpy.abs(tgt-ref_tgt))/(1.0+numpy.max([numpy.abs(tgt), numpy.abs(ref_tgt)]))
    assert error < 5e-05


