
import pylibxc
import pytest
import numpy
from pylibxc.example_densities import test_data


def test_gga_x_lbm_Li_2_vrho():
    # Prepare the input
    inp = test_data["Li"]

    # Get the functional
    feval = pylibxc.LibXCFunctional("gga_x_lbm", 2)

    # Evaluate the data
    out = feval.compute(inp, do_exc=False, do_vxc=True, do_fxc=False, do_kxc=False, do_lxc=False)
    tgt = out["vrho"].flatten()
    ref_tgt = numpy.asarray([-2.907641229044895e+00, -2.910011331640515e+00, -2.107874693410321e+00, -2.109476101862640e+00, -7.647197455489041e-01, -7.650909860619749e-01, -2.575744036411183e-01, -2.618027596817114e-01, -1.403286286562327e-01, -1.071609409949035e-01, -2.636744875527430e-01, -2.668796364246706e-01, -1.103281969540964e-01, -9.837510551176550e-02])
    error = numpy.max(numpy.abs(tgt-ref_tgt))/(1.0+numpy.max([numpy.abs(tgt), numpy.abs(ref_tgt)]))
    assert error < 5e-05


